from .part1 import *
import pytest


@pytest.mark.parametrize(
    "line, expected",
    [
        (".....", "....."),
        ("...O.", "O...."),
        ("..O.O", "OO..."),
        (".#.O.", ".#O.."),
        ("O#..O", "O#O.."),
        ("OO.O.O..##", "OOOO....##"),
        ("...OO....O", "OOO......."),
        (".O...#O..O", "O....#OO.."),
        (".O.#......", "O..#......"),
        (".#.O......", ".#O......."),
        ("#.#..O#.##", "#.#O..#.##"),
        ("..#...O.#.", "..#O....#."),
        ("....O#.O#.", "O....#O.#."),
        ("....#.....", "....#....."),
        (".#.O.#O...", ".#O..#O..."),
        ("#OO......#", "#OO......#"),
        (
            "..#...O.........#.#.OO.#...#..O.#..#.O..O.#O.O..O.O....#..#................##.#O#.OO...O..O#OO.....#",
            "..#O............#.#OO..#...#O...#..#OO....#OOOO........#..#................##.#O#OOOO......#OO.....#",
        ),
        ("##.#O#.OO...O..O#OO.....#", "##.#O#OOOO......#OO.....#"),
        (".O#OO.....#", "O.#OO.....#"),
    ],
)
def test_roll_west(line, expected):
    actual = roll_row_west(line)
    assert actual == expected
